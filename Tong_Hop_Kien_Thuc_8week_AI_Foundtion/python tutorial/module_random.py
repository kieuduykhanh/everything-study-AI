# ramdom doc: https://docs.python.org/3/library/random.html

import random
# các hàm random thường dùng

# hàm random.random(): trả về 1 số thực ngẫu nhiên trong khoảng [0, 1]
print('số ngẫu nhiên trong khoảng [0, 1]: ', random.random())

# hàm random.randint(a, b): trả về một số nguyên bất kì trong khoảng [a, b]
print('số nguyên ngẫu nhiên trong khoảng [1, 10]: ', random.randint(1, 10))